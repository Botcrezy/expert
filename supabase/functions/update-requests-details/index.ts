Deno.serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const supabaseAdmin = createClient(
      Deno.env.get("SUPABASE_URL")!,
      Deno.env.get("SUPABASE_SERVICE_ROLE_KEY")!
    );

    const userId = "76096b98-e2d3-4a78-adac-88498932cdb1";

    // Fetch all requests
    const { data: requests, error: fetchErr } = await supabaseAdmin
      .from("requests")
      .select("id, title, category_id, created_at")
      .eq("user_id", userId)
      .order("created_at", { ascending: true });

    if (fetchErr) throw fetchErr;

    // Fetch categories
    const { data: categories } = await supabaseAdmin
      .from("categories")
      .select("id, name_ar")
      .eq("is_active", true);

    const catMap = new Map<string, string>();
    categories?.forEach((c: any) => catMap.set(c.id, c.name_ar));

    // Track usage per category
    const catUsageIndex = new Map<string, number>();

    let updated = 0;
    const batchSize = 25;

    for (let i = 0; i < requests!.length; i += batchSize) {
      const batch = requests!.slice(i, i + batchSize);
      const promises = batch.map(async (req: any) => {
        const catId = req.category_id;
        const catName = catMap.get(catId) || "خدمة متخصصة";
        const currentIndex = catUsageIndex.get(catId) || 0;
        catUsageIndex.set(catId, currentIndex + 1);

        let newTitle: string;
        let newDescription: string;

        const content = categoryContent[catId];
        if (content) {
          const titleIdx = currentIndex % content.titles.length;
          const descIdx = currentIndex % content.descriptions.length;
          newTitle = content.titles[titleIdx];
          newDescription = content.descriptions[descIdx];
        } else {
          const def = getDefaultContent(catName, currentIndex);
          newTitle = def.title;
          newDescription = def.description;
        }

        // Set deadline 3-7 days from created_at
        const createdAt = new Date(req.created_at);
        const daysToAdd = 3 + Math.floor(Math.random() * 5); // 3-7 days
        const deadline = new Date(createdAt.getTime() + daysToAdd * 24 * 60 * 60 * 1000);

        const { error: updateErr } = await supabaseAdmin
          .from("requests")
          .update({
            title: newTitle,
            description: newDescription,
            deadline: deadline.toISOString(),
          })
          .eq("id", req.id);

        if (updateErr) {
          console.error(`Failed to update ${req.id}:`, updateErr.message);
          return false;
        }
        return true;
      });

      const results = await Promise.all(promises);
      updated += results.filter(Boolean).length;
    }

    return new Response(
      JSON.stringify({ success: true, updated, total: requests!.length }),
      { headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  } catch (err: any) {
    return new Response(
      JSON.stringify({ error: err.message }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});
