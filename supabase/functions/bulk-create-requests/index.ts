serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const supabaseUrl = Deno.env.get("SUPABASE_URL")!;
    const supabaseServiceKey = Deno.env.get("SUPABASE_SERVICE_ROLE_KEY")!;
    const supabaseAdmin = createClient(supabaseUrl, supabaseServiceKey);

    const body = await req.json();
    const { user_id, num_orders = 500 } = body;

    if (!user_id) {
      return new Response(JSON.stringify({ error: "user_id is required" }), {
        status: 400,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    // Fetch all active categories
    const { data: categories, error: catError } = await supabaseAdmin
      .from("categories")
      .select("id, name, name_ar")
      .eq("is_active", true)
      .order("sort_order");

    if (catError || !categories || categories.length === 0) {
      return new Response(JSON.stringify({ error: "Failed to fetch categories" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    // Check user's credits
    const { data: subData } = await supabaseAdmin
      .from("client_subscriptions")
      .select("id, credits_remaining")
      .eq("user_id", user_id)
      .eq("is_active", true)
      .order("created_at", { ascending: false })
      .limit(1)
      .single();

    const { data: walletData } = await supabaseAdmin
      .from("credits_ledger")
      .select("balance_after")
      .eq("user_id", user_id)
      .order("created_at", { ascending: false })
      .limit(1)
      .single();

    const subCredits = subData?.credits_remaining || 0;
    const walletCredits = walletData?.balance_after || 0;
    const totalCredits = subCredits + walletCredits;
    const creditsNeeded = num_orders * 10;

    console.log(`User credits: sub=${subCredits}, wallet=${walletCredits}, total=${totalCredits}, needed=${creditsNeeded}`);

    if (totalCredits < creditsNeeded) {
      return new Response(JSON.stringify({ 
        error: "Insufficient credits", 
        available: totalCredits, 
        needed: creditsNeeded 
      }), {
        status: 400,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    console.log(`Creating ${num_orders} orders across ${categories.length} categories for user ${user_id}`);

    // Create all orders by directly inserting into the database
    const results = { success: 0, failed: 0, errors: [] as string[] };
    
    // Process in batches of 25
    const batchSize = 25;
    for (let batchStart = 0; batchStart < num_orders; batchStart += batchSize) {
      const batchEnd = Math.min(batchStart + batchSize, num_orders);
      const requests = [];

      for (let i = batchStart; i < batchEnd; i++) {
        const category = categories[i % categories.length];
        const catNameAr = category.name_ar;
        const templates = orderTemplates[catNameAr];

        let title: string;
        let description: string;

        if (templates && templates.length > 0) {
          title = templates[i % templates.length];
        } else {
          title = genericTitles[i % genericTitles.length].replace(/\{category\}/g, catNameAr);
        }

        description = genericDescriptions[i % genericDescriptions.length];
        title = title + ` #${String(i + 1).padStart(3, "0")}`;

        requests.push({
          id: crypto.randomUUID(),
          user_id: user_id,
          category_id: category.id,
          title: title,
          description: description,
          size: "large",
          credits_cost: 10,
          status: "submitted",
          files: [],
          idempotency_key: `bulk-${user_id}-${i}-${Date.now()}`,
        });
      }

      // Batch insert requests
      const { error: insertError } = await supabaseAdmin
        .from("requests")
        .insert(requests);

      if (insertError) {
        console.error(`Batch insert error at ${batchStart}:`, insertError);
        results.failed += requests.length;
        results.errors.push(`Batch ${batchStart}-${batchEnd}: ${insertError.message}`);
      } else {
        results.success += requests.length;
      }
    }

    // Deduct credits from subscription
    if (results.success > 0) {
      const totalCost = results.success * 10;
      let remaining = totalCost;

      // Deduct from subscription first
      if (subData && subCredits > 0) {
        const deductFromSub = Math.min(remaining, subCredits);
        await supabaseAdmin
          .from("client_subscriptions")
          .update({ credits_remaining: subCredits - deductFromSub, updated_at: new Date().toISOString() })
          .eq("id", subData.id);
        remaining -= deductFromSub;
      }

      // Deduct rest from wallet
      if (remaining > 0) {
        const newWalletBalance = walletCredits - remaining;
        await supabaseAdmin.from("credits_ledger").insert({
          user_id: user_id,
          type: "request_debit",
          amount: -remaining,
          balance_after: newWalletBalance,
          reason: `خصم كريديت لإنشاء ${results.success} طلب (دفعة واحدة)`,
          reference_type: "bulk_requests",
          reference_id: crypto.randomUUID(),
        });
      }

      // Add notification
      await supabaseAdmin.from("notifications").insert({
        user_id: user_id,
        type: "request",
        title: "تم إنشاء طلبات متعددة",
        body: `تم إنشاء ${results.success} طلب بنجاح وخصم ${totalCost} كريديت من حسابك.`,
        reference_type: "bulk_requests",
      });
    }

    console.log(`Bulk creation complete: ${results.success} success, ${results.failed} failed`);

    return new Response(JSON.stringify({ success: true, results }), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  } catch (error) {
    console.error("Bulk create error:", error);
    return new Response(JSON.stringify({ error: error.message }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
